    private void preencheTabela(){
        //criar uma variavel "AlunoDao"
        //pegar os dados dos alunos da lista e jogar dentro da tabela
        AlunoDAO alunoDAO = new AlunoDAO();
        List<Aluno> listaAlunos = alunoDAO.getAlunos();
        DefaultTableModel tabelaAlunos = (DefaultTableModel) tblAlunos.getModel();
        //limpar a tabela para preencher com os novos dados
        tabelaAlunos.setNumRows(0);
        
        //percorrer o "listaAlunos" e inserir na "tabelaCursos"
        for (Aluno a : listaAlunos) {
            Object[] obj = new Object[]{
                //id, nomealuno, nomecurso... o array de objetos segue a ordem da tabela no banco de dados
                a.getId(), a.getNomeAluno(), a.getCursoid().getNomeCurso()
            };
            tabelaAlunos.addRow(obj);
        }
    }
